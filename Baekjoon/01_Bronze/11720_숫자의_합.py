# 숫자의 합

# N개의 숫자가 공백 없이 쓰여있다. 이 숫자를 모두 합해서 출력하는 프로그램을 작성하시오.

# 첫째 줄에 숫자의 개수 N (1 ≤ N ≤ 100)이 주어진다. 둘째 줄에 숫자 N개가 공백없이 주어진다.

N = int(input())            # 숫자의 개수
num = list(input())         # 숫자를 받지만 각자리의 숫자를 구하기 위해 리스트로 받는다
hap = 0                     # 각자리숫자의 합
for j in range(N):          # 길이만큼 반복
    hap += int(num[j])      # 각자리의 숫자가 str 이므로 int 로 바꿔서 더하기
print(hap)